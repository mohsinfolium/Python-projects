# For loop
print("For loop example:")
for i in range(5):
    print(f"Iteration {i + 1}")

# While loop
print("\nWhile loop example:")
count = 0
while count < 5:
    print(f"Count is {count}")
    count += 1

# Nested loops
print("\nNested loop example:")
for i in range(3):
    for j in range(2):
        print(f"Outer loop {i}, Inner loop {j}")
