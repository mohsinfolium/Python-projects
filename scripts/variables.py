# Variable declaration
name = "Alice"  # String
age = 25        # Integer
height = 5.7    # Float
is_student = True  # Boolean

# Printing variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# Operations with variables
age_next_year = age + 1
print("Age next year:", age_next_year)

full_description = f"{name} is {age} years old and {height} feet tall."
print(full_description)

# Changing variable values
name = "Bob"
print("Updated Name:", name)